/**
 * Clase que representa una calculadora con las 4 operaciones básicas de dos números
 */
public class Calculadora {

    public int obtenerSuma(int num1, int num2) {
        return num1 + num2;
    }

    public int obtenerResta(int num1, int num2) {
        return num1 - num2;
    }

    public int obtenerMultiplicacion(int num1, int num2) {
        return num1 * num2;
    }

    public double obtenerDivisión(int num1, int num2) {
        return num1 / num2;
    }

}
